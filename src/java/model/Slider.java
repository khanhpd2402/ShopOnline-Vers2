package model;

/**
 *
 * @author khanh
 */
public class Slider {

    private int id;
    private String img;
    private String title;
    private String description;
    private int status;

    public Slider() {
    }

    public Slider(int id, String img, String title, String description, int status) {
        this.id = id;
        this.img = img;
        this.title = title;
        this.description = description;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

}
